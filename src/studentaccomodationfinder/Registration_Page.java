    private boolean isValidEmail(String email) {
        String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        Pattern pattern = Pattern.compile(emailRegex);
        Matcher matcher = pattern.matcher(email);
        return matcher.matches();
    }
    
    
    private void register_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_register_btnActionPerformed
        // TODO add your handling code here:
    // Get values from text fields
    String username = username_field.getText();
    String email = email_field.getText();
    String password1 = password_one.getText();
    String password2 = password_two.getText();

    // Validate the input (e.g., check if fields are empty, passwords match, etc.)
if (username.isEmpty() || email.isEmpty() || password1.isEmpty()) {
    // Show error message
    JOptionPane.showMessageDialog(this, "Please fill all fields");
    return;
}

if (!isValidEmail(email)) {
    JOptionPane.showMessageDialog(this, "Invalid email format");
    return;
}

if (!password1.equals(password2)) {
    // Show error message
    JOptionPane.showMessageDialog(this, "Passwords do not match");
    return;
}        
    try{
        // If input is valid, proceed with the registration process
     Database.addNewUser(username, email, password1);
    JOptionPane.showMessageDialog(this, "Registration successful"); 
    
        // Close the current registration window and open the Sign-In page
  
        this.dispose();
        Login_Interface signInPage = new Login_Interface();
        signInPage.setVisible(true);
    } catch (Exception e) {
        // If an exception occurs, show an error message
        JOptionPane.showMessageDialog(this, "Registration failed: " + e.getMessage());
    }
 
    
    
    }//GEN-LAST:event_register_btnActionPerformed

