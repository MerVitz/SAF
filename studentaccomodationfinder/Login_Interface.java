    private void SignIn_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignIn_BtnActionPerformed
        // TODO add your handling code here:
        String username = username_field.getText();
        String password = new String(passwordfield.getPassword());
        //Valid Input checking
    if (username.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter both username and password");
        return;
    }    
        // Authenticate user
    boolean isAuthenticated = Database.authenticateUser(username, password);
    if (isAuthenticated) {
        
        SessionManager.isUserSignedIn = true; // Update sign-in status        
        // User authenticated successfully
        // Open the main application window and close the login window
        MainAppWindow mainApp = new MainAppWindow();
        mainApp.setVisible(true);
        this.dispose();
    } else {
        // Authentication failed
        JOptionPane.showMessageDialog(this, "Invalid username or password");
    }    
    
    
    

    }//GEN-LAST:event_SignIn_BtnActionPerformed

